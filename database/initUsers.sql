    RETURNS trigger AS $$
BEGIN
    IF (NEW.password != OLD.password) THEN
        NEW.password = encode(hmac(NEW.password,'Wow, what a marvelous key here','sha256'), 'base64');
END IF;
RETURN NEW;
END;
$$ LANGUAGE plpgsql;

